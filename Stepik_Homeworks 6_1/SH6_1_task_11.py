a, b, c, = int(input()), int(input()), int(input())

min_var = 0
ave_var = 0
max_var = 0

if a < b:
    min_var = a
else:
    min_var = b
if c < min_var:
    min_var = c

if a > b:
    max_var = a
else:
    max_var = b

